function makeProducts(
  category: string,
  items: {
    name: string;
    desc: string;
    price: number;
    unit: string;
    badge?: string;
  }[],
): Product[] {
  const pool =
    CATEGORY_PHOTO_POOLS[category] ?? CATEGORY_PHOTO_POOLS.vegetables;
  return items.map((item, i) => {
    const photoId = pool[i % pool.length];
    const imageUrl = `https://images.unsplash.com/photo-${photoId}?w=400&h=400&fit=crop&auto=format`;
    return {
      id: `${category}-${i + 1}`,
      name: item.name,
      description: item.desc,
      price: item.price,
      category,
      unit: item.unit,
      inStock: i % 12 !== 0,
      imageUrl,
      badge: item.badge,
    };
  });
}

